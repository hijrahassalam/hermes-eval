/**
 * Generate a plain-text report from suite results.
 *
 * @param {import('./runner.js').SuiteResult} results
 * @returns {string}
 */
export function generateTextReport(results) {
  const { summary, results: testResults } = results;
  const lines = [];

  lines.push('═'.repeat(60));
  lines.push('  HERMES-EVAL — Test Suite Report');
  lines.push('═'.repeat(60));
  lines.push('');

  // Summary
  lines.push('📊 SUMMARY');
  lines.push('─'.repeat(40));
  lines.push(`  Total tests:    ${summary.total}`);
  lines.push(`  Passed:         ${summary.passed} ✅`);
  lines.push(`  Failed:         ${summary.failed} ❌`);
  lines.push(`  Errors:         ${summary.errors} ⚠️`);
  lines.push(`  Pass rate:      ${(summary.passRate * 100).toFixed(1)}%`);
  lines.push(`  Avg score:      ${summary.avgScore}/10`);
  lines.push(`  Score range:    ${summary.minScore} – ${summary.maxScore}`);
  lines.push(`  Avg latency:    ${summary.avgLatencyMs}ms`);
  lines.push(`  Total time:     ${(summary.totalLatencyMs / 1000).toFixed(1)}s`);
  lines.push('');

  // Pass rate bar
  const barWidth = 40;
  const filled = Math.round(summary.passRate * barWidth);
  const bar = '█'.repeat(filled) + '░'.repeat(barWidth - filled);
  lines.push(`  [${bar}] ${(summary.passRate * 100).toFixed(1)}%`);
  lines.push('');

  // Per-test breakdown
  lines.push('📋 TEST DETAILS');
  lines.push('─'.repeat(40));

  // Sort by score ascending (worst first)
  const sorted = [...testResults].sort((a, b) => a.score - b.score);

  for (const r of sorted) {
    const status = r.error ? '⚠️ ERR' : r.passed ? '✅ PASS' : '❌ FAIL';
    const id = r.testCaseId.length > 25 ? r.testCaseId.slice(0, 22) + '...' : r.testCaseId.padEnd(25);
    lines.push(`  ${status}  ${id}  Score: ${r.score}/10  ${r.latencyMs}ms`);
  }

  lines.push('');

  // Worst cases (bottom 5)
  const worst = sorted.slice(0, Math.min(5, sorted.length));
  if (worst.length > 0 && worst[0].score < 7) {
    lines.push('🔍 WORST PERFORMING TESTS');
    lines.push('─'.repeat(40));
    for (const r of worst) {
      if (r.score >= 7) break;
      lines.push(`  • ${r.testCaseId} (score: ${r.score}/10)`);
      lines.push(`    Input: ${r.input.slice(0, 80)}${r.input.length > 80 ? '...' : ''}`);
      if (r.reasoning) {
        const reason = r.reasoning.split('\n')[0].slice(0, 100);
        lines.push(`    Reason: ${reason}`);
      }
      lines.push('');
    }
  }

  // Best cases (top 5)
  const best = sorted.slice(-5).reverse();
  if (best.length > 0 && best[0].score >= 8) {
    lines.push('🌟 BEST PERFORMING TESTS');
    lines.push('─'.repeat(40));
    for (const r of best) {
      if (r.score < 8) break;
      lines.push(`  • ${r.testCaseId} (score: ${r.score}/10)`);
    }
    lines.push('');
  }

  lines.push('═'.repeat(60));
  lines.push(`  Generated by hermes-eval at ${new Date().toISOString()}`);
  lines.push('═'.repeat(60));

  return lines.join('\n');
}

